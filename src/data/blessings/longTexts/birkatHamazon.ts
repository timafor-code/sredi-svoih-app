function createBirkatHamazonChabadTranslitBlocks({
  sourceByHebrewKey,
  suffix,
  translitNusach,
}: {
  sourceByHebrewKey: BirkatHamazonChabadTranslitSourceByHebrewKey;
  suffix: 'translit_ashkenaz' | 'translit_sephard';
  translitNusach: BlessingTranslitNusach;
}): BlessingContentBlock[] {
  return birkatHamazonChabadHebrewBlocks.map((block) => {
    const contentBlock: BlessingContentBlock = block;
    const source = sourceByHebrewKey[block.key];
    const runtimeMetadata = birkatHamazonChabadRuntimeMetadata[block.key] ?? {};

    if (contentBlock.segments && source.segments?.length !== contentBlock.segments.length) {
      throw new Error(`Missing translit segments for ${block.key} / ${translitNusach}`);
    }

    const segments = contentBlock.segments
      ? contentBlock.segments.map((segment, index) => ({
          annotationRu: segment.annotationRu,
          bodyRu: source.segments?.[index]?.bodyRu ?? '',
        }))
      : undefined;

    return {
      ...contentBlock,
      ...runtimeMetadata,
      key: block.key.replace(/_he$/, `_${suffix}`),
      language: 'translit',
      translitNusach,
      bodyRu: source.bodyRu,
      segments,
      dayNameByFlag: source.dayNameByFlag ?? runtimeMetadata.dayNameByFlag,
    };
  });
}

